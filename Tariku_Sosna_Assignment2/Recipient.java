/**
 * recipient class
 * 
 * @author tz_as
 *
 */
public class Recipient {
	String name;

	/**
	 * recipient constructor with name
	 * 
	 * @param name name
	 */
	public Recipient(String name) {
		this.name = name;
	}

	/**
	 * get the name
	 * 
	 * @return name
	 */
	public String getName() {
		return name;

	}

	/**
	 * get the name
	 * 
	 * @return name
	 */
	public String toString() {
		return name;
	}
}
